/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */

  // Read fault status registers
  volatile uint32_t cfsr = SCB->CFSR;   // Configurable Fault Status Register
  volatile uint32_t hfsr = SCB->HFSR;   // HardFault Status Register
  volatile uint32_t mmfar = SCB->MMFAR; // MemManage Fault Address Register
  volatile uint32_t bfar = SCB->BFAR;   // BusFault Address Register

  printf("\n\n*** HARDFAULT DETECTED ***\n");
  printf("CFSR:  0x%08lX\n", cfsr);
  printf("HFSR:  0x%08lX\n", hfsr);
  printf("MMFAR: 0x%08lX\n", mmfar);
  printf("BFAR:  0x%08lX\n", bfar);

  // Decode CFSR
  if (cfsr & (1 << 7))  printf("  -> MMARVALID: MMFAR contains valid address\n");
  if (cfsr & (1 << 4))  printf("  -> MSTKERR: Stacking error\n");
  if (cfsr & (1 << 3))  printf("  -> MUNSTKERR: Unstacking error\n");
  if (cfsr & (1 << 1))  printf("  -> DACCVIOL: Data access violation\n");
  if (cfsr & (1 << 0))  printf("  -> IACCVIOL: Instruction access violation\n");

  if (cfsr & (1 << 15)) printf("  -> BFARVALID: BFAR contains valid address\n");
  if (cfsr & (1 << 12)) printf("  -> STKERR: Bus fault on stacking\n");
  if (cfsr & (1 << 11)) printf("  -> UNSTKERR: Bus fault on unstacking\n");
  if (cfsr & (1 << 10)) printf("  -> IMPRECISERR: Imprecise data access error\n");
  if (cfsr & (1 << 9))  printf("  -> PRECISERR: Precise data access error\n");
  if (cfsr & (1 << 8))  printf("  -> IBUSERR: Instruction bus error\n");

  printf("System crashed during ITTIA DB initialization\n");

  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    HAL_GPIO_TogglePin(GPIOF, GPIO_PIN_1);
    HAL_Delay(100);
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

